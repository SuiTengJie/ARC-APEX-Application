float softmax(float data[output_node], int index)
{
	float sum = 0;
	for(int k = 0; k < output_node; k++)
		sum += exp(data[k]);
	return (float)exp(data[index])/sum;
}

int argmax(float data[output_node])
{
	int max_index = 0;
	float max_value = data[0];
	for(int i = 1; i < output_node; i++)
	{
		if(data[i] > max_value)
		{
			max_value = data[i];
			max_index = i;
		}
	}
	return max_index;
}

